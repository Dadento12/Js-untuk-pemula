// Deklarasi variabel menggunakan var
// `var` adalah cara lama untuk mendeklarasikan variabel. Variabel yang dideklarasikan dengan `var` dapat diubah nilainya dan memiliki cakupan fungsi atau global.
var nama = "Alice";
console.log(nama);

// Deklarasi variabel menggunakan let
// `let` digunakan untuk mendeklarasikan variabel dengan cakupan scope. Variabel yang dideklarasikan dengan `let` dapat diubah nilainya dan lebih disukai daripada `var` karena memiliki cakupan yang lebih ketat.
let umur = 25;
console.log(umur);

// Deklarasi variabel menggunakan const
// `const` digunakan untuk mendeklarasikan variabel konstan yang nilainya tidak dapat diubah setelah dideklarasikan. `const` juga memiliki cakupan blok atau scope.
const negara = "Indonesia";
console.log(negara);
